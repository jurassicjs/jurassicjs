/**
 * Roar function
 * @param phrase - The phrase to roar
 * @returns The roared phrase
 */
export function roar(phrase: string): string {
  return `${phrase.toUpperCase()} !!! ROAR!`
}

/**
 * Coo function
 * @param phrase - The phrase to coo
 * @returns The coo phrase
 */
export function coo(phrase: string): string {
  return `${phrase.toLowerCase()} coo`
}
